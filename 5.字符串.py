'''
5.4编码和解码
编码 encode()：字符串（str）→ 字节串（bytes）
人类可读的文字 → 计算机存储 / 传输的二进制数据
解码 decode()：字节串（bytes）→ 字符串（str）
二进制数据 → 人类可读文字
'''

a = "好累啊"
print(a.encode("utf-8"))    #b'\xe5\xa5\xbd\xe7\xb4\xaf\xe5\x95\x8a'
b = b'\xe5\xa5\xbd\xe7\xb4\xaf\xe5\x95\x8a'
print(b.decode())   #好累啊

